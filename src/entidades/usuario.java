package entidades;


public class usuario {
    
    private String IdentificacionUsuario;
    private String nombreCompletoUsuario;
    private String generoUsuario;
    private String correoElectronicoUsuario;
    private String tipoTarjetaUsuario;
    private String numeroTarjetaUsuario;
    private String contrasenaUsuario;
    private String perfilUsuario;

    public String getIdentificacionUsuario() {
        return IdentificacionUsuario;
    }

    public void setIdentificacionUsuario(String IdentificacionUsuario) {
        this.IdentificacionUsuario = IdentificacionUsuario;
    }

    public String getNombreCompletoUsuario() {
        return nombreCompletoUsuario;
    }

    public void setNombreCompletoUsuario(String nombreCompletoUsuario) {
        this.nombreCompletoUsuario = nombreCompletoUsuario;
    }

    public String getGeneroUsuario() {
        return generoUsuario;
    }

    public void setGeneroUsuario(String generoUsuario) {
        this.generoUsuario = generoUsuario;
    }

    public String getCorreoElectronicoUsuario() {
        return correoElectronicoUsuario;
    }

    public void setCorreoElectronicoUsuario(String correoElectronicoUsuario) {
        this.correoElectronicoUsuario = correoElectronicoUsuario;
    }

    public String getTipoTarjetaUsuario() {
        return tipoTarjetaUsuario;
    }

    public void setTipoTarjetaUsuario(String tipoTarjetaUsuario) {
        this.tipoTarjetaUsuario = tipoTarjetaUsuario;
    }

    public String getNumeroTarjetaUsuario() {
        return numeroTarjetaUsuario;
    }

    public void setNumeroTarjetaUsuario(String numeroTarjetaUsuario) {
        this.numeroTarjetaUsuario = numeroTarjetaUsuario;
    }

    public String getContrasenaUsuario() {
        return contrasenaUsuario;
    }

    public void setContrasenaUsuario(String contrasenaUsuario) {
        this.contrasenaUsuario = contrasenaUsuario;
    }

    public String getPerfilUsuario() {
        return perfilUsuario;
    }

    public void setPerfilUsuario(String perfilUsuario) {
        this.perfilUsuario = perfilUsuario;
    }

    

   
    
}
