    public void cargarComboBox() {

        generosMusicalesVarios gmv = new generosMusicalesVarios();

        ArrayList<generosMusicales> generosMusicales;

        generosMusicales = gmv.actualizarGeneroToCombo();

        if (generosMusicales != null) {

            jcGenerosMusicalesActualizarGetCodigoGenero.removeAllItems();

            jcGenerosMusicalesActualizarGetCodigoGenero.addItem("Seleccione un Codigo de Genero");

            for (int i = 0; i < generosMusicales.size(); i++) {

                jcGenerosMusicalesActualizarGetCodigoGenero.addItem(generosMusicales.get(i).getCodigoGenero());

            }

        }

    }

    public void llenadoCampoTexto() {
        String descGenero = "";
        generosMusicalesVarios gmv = new generosMusicalesVarios();

        ArrayList<generosMusicales> generosMusicales;

        generosMusicales = gmv.actualizarGeneroToCombo();

        if (jcGenerosMusicalesActualizarGetCodigoGenero.getSelectedIndex() == 0) {
            jtGenerosMusicalesActualizarGetDescGenero.setText("");
        }

        for (int i = 0; i < generosMusicales.size(); i++) {
            if (generosMusicales.get(i).getCodigoGenero().equals(jcGenerosMusicalesActualizarGetCodigoGenero.getSelectedItem())) {
                descGenero = generosMusicales.get(i).getDescripcionGenero();
                jtGenerosMusicalesActualizarGetDescGenero.setText(descGenero);
            }
        }

    }
