    public void cargarComboBox() {

        cancionesVarios c1 = new cancionesVarios();

        ArrayList<canciones> cancionesDetalles;

        cancionesDetalles = c1.actualizarCancionesToCombo();

        generosMusicalesVarios gmv = new generosMusicalesVarios();
        ArrayList<generosMusicales> generosMusicales;

        generosMusicales = gmv.actualizarGeneroToCombo();

        if (cancionesDetalles != null) {

            jcCancionesActualizarCanciones.addItem("Seleccione Codigo de Cancion");

            for (int i = 0; i < cancionesDetalles.size(); i++) {
                jcCancionesActualizarCanciones.addItem(cancionesDetalles.get(i).getCodigoCancion());
            }
        }

        if (generosMusicales != null) {

            jcCancionesActualizarGenero.addItem("Seleccione Codigo de Genero");

            for (int i = 0; i < generosMusicales.size(); i++) {
                jcCancionesActualizarGenero.addItem(generosMusicales.get(i).getCodigoGenero());
            }
        }

    }

    public void llenadoCamposTexto() {
        String nombreCancion = "";
        Double precioCancion = 0.0;
        String codigoGenero = "";
        String descGenero = "";

        cancionesVarios c1 = new cancionesVarios();

        ArrayList<canciones> canciones;

        canciones = c1.actualizarCancionesToCombo();

        if (jcCancionesActualizarCanciones.getSelectedIndex() == 0) {
            jtCancionesActualizarGetNombreCancion.setText("");
            jtCancionesActualizarGetCodigoGenero.setText("");
            jtCancionesActualizarGetPrecioCancion1.setText("");
            jtCancionesActualizarGetDescGenero.setText("");
        }
        for (int i = 0; i < canciones.size(); i++) {

            if (canciones.get(i).getCodigoCancion().equals(jcCancionesActualizarCanciones.getSelectedItem())) {
                nombreCancion = canciones.get(i).getNombreCancion();
                precioCancion = canciones.get(i).getPrecioCancion();
                codigoGenero = canciones.get(i).getCodigoGeneroCancion();
                descGenero = canciones.get(i).getDescripcionGeneroCancion();
                jtCancionesActualizarGetNombreCancion.setText(nombreCancion);
                jtCancionesActualizarGetCodigoGenero.setText(codigoGenero);
                jtCancionesActualizarGetDescGenero.setText(descGenero);
                jtCancionesActualizarGetPrecioCancion1.setText(String.valueOf(precioCancion));
            }
        }

    }

