    private void jlCancionesActualizarBotonActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlCancionesActualizarBotonActualizarMouseClicked
        if (jcCancionesActualizarCanciones.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Por Favor Seleccione un Codigo de Cancion", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
        } else {
            if (jcCancionesActualizarGenero.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Por Favor Seleccione un Codigo de Genero", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
            } else {
                if (jtCancionesActualizarSetNombreCancion.getText().length() == 0) {
                    JOptionPane.showMessageDialog(null, "Por Favor Ingrese el Nombre de la Cancion", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
                } else {
                    if (jtCancionesActualizarSetNombreCancion.getText().length() == 0) {
                        JOptionPane.showMessageDialog(null, "Por Favor Ingrese el Nombre de la Cancion", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
                    } else {
                        if (jtCancionesActualizarSetPrecioCancion.getText().length() == 0) {
                            JOptionPane.showMessageDialog(null, "Por Favor Ingrese el Precio de la Cancion", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
                        } else {
                            canciones c1 = new canciones();
                            cancionesVarios cv = new cancionesVarios();

                            ArrayList<canciones> canciones;
                            canciones = cv.actualizarCancionesToCombo();

                            boolean cancionRepetida = false;

                            for (int i = 0; i < canciones.size(); i++) {

                                if (canciones.get(i).getNombreCancion().equals(jtCancionesActualizarSetNombreCancion.getText())) {
                                    cancionRepetida = true;
                                    break;
                                }
                            }

                            if (cancionRepetida == true) {
                                JOptionPane.showMessageDialog(null, "Cancion ya existe, favor intentar de nuevo", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
                            } else {
                                c1.setCodigoCancion(jcCancionesActualizarCanciones.getSelectedItem().toString());
                                c1.setNombreCancion(jtCancionesActualizarSetNombreCancion.getText().toString());
                                c1.setCodigoGeneroCancion(jcCancionesActualizarGenero.getSelectedItem().toString());
                                c1.setPrecioCancion(Double.parseDouble(jtCancionesActualizarSetPrecioCancion.getText()));
                                JOptionPane.showMessageDialog(null, "Cancion actualizada correctamente", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
                            }

                            if (cv.actualizarCancion(c1) == true) {                                
                                jtCancionesActualizarGetNombreCancion.setText("");
                                jtCancionesActualizarGetPrecioCancion1.setText("");
                                jtCancionesActualizarGetCodigoGenero.setText("");
                                jtCancionesActualizarSetNombreCancion.setText("");
                                jtCancionesActualizarSetPrecioCancion.setText("");
                                jtCancionesActualizarGetDescGenero.setText("");
                                jcCancionesActualizarCanciones.setSelectedItem("Seleccione Codigo de Cancion");
                                jcCancionesActualizarGenero.setSelectedItem("Seleccione Codigo de Genero");

                            } else {
                                JOptionPane.showMessageDialog(null, "Falló la actualizacion de la cancion", "La Venta Musical", JOptionPane.INFORMATION_MESSAGE, m);
                                jtCancionesActualizarGetNombreCancion.setText("");
                                jtCancionesActualizarGetPrecioCancion1.setText("");
                                jtCancionesActualizarGetCodigoGenero.setText("");
                                jtCancionesActualizarSetNombreCancion.setText("");
                                jtCancionesActualizarSetPrecioCancion.setText("");
                                jtCancionesActualizarGetDescGenero.setText("");
                                jcCancionesActualizarCanciones.setSelectedItem("Seleccione Codigo de Cancion");
                                jcCancionesActualizarGenero.setSelectedItem("Seleccione Codigo de Genero");
                            }

                        }
                    }
                }

            }
        }
    }//GEN-LAST:event_jlCancionesActualizarBotonActualizarMouseClicked

