    public void consultaGeneros() {
        generosMusicalesVarios gmv = new generosMusicalesVarios();
        ArrayList<generosMusicales> generosMusicales;

        generosMusicales = gmv.actualizarGeneroToCombo();

        if (generosMusicales != null) {

            DefaultTableModel modelo = new DefaultTableModel();

            String[] nombresColumnas = {"Codigo Genero", "Descripcion Genero"};

            modelo.setColumnIdentifiers(nombresColumnas);

            Object[] columna = new Object[modelo.getColumnCount()];

            for (int i = 0; i < generosMusicales.size(); i++) {

                columna[0] = generosMusicales.get(i).getCodigoGenero();
                columna[1] = generosMusicales.get(i).getDescripcionGenero();

                modelo.addRow(columna);
            }

            jtConsultaGeneros.setModel(modelo);

        }
    }
